package frc.robot.autos.preset;

import com.pathplanner.lib.path.PathPlannerTrajectory;
import edu.wpi.first.math.geometry.Pose2d;
import edu.wpi.first.wpilibj2.command.Command;
import frc.robot.RobotContainer;
import java.util.List;

/** Interface for classes that build preset autonomous routines */
public interface PresetAutoBuilder {

  /** Returns a Command that implements the auto routine */
  public Command getCommand(RobotContainer botContainer);

  /** Returns the initial pose of the auto routine */
  public Pose2d getInitialPose();

  /**
   * Returns a list of trajectories implemented by the auto routine for display on dashboard
   * software
   */
  public List<PathPlannerTrajectory> getTrajectories();
}
