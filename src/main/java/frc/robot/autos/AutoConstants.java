package frc.robot.autos;

/** Constants for use in autonomous routines */
public class AutoConstants {

  public static enum AutoPreset {
    Example("Example", 0);

    public final String name;
    public final int id;

    private AutoPreset(String name, int id) {
      this.name = name;
      this.id = id;
    }

    /** Returns a list of names of enum elements */
    public static String[] getNames() {
      return new String[] {Example.name};
    }
    ;
  }
}
