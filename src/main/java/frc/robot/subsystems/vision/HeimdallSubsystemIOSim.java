package frc.robot.subsystems.vision;

import edu.wpi.first.math.geometry.Rotation2d;
import org.photonvision.EstimatedRobotPose;
import org.photonvision.PhotonCamera;
import org.photonvision.simulation.PhotonCameraSim;
import org.photonvision.simulation.SimCameraProperties;
import org.photonvision.simulation.VisionSystemSim;

/** Add your docs here. */
public class HeimdallSubsystemIOSim extends HeimdallSubsystemIOReal {
  /** Simulated PhotonCamera */
  private PhotonCameraSim cameraSim;
  /** Simulated PhotonVision system */
  private VisionSystemSim visionSim;

  //////////////////////////////////////////////////////////////////////////////////////////////////
  /**
   * Creates an instance of the I/O
   *
   * @param camera Camera used by the vision subsystem I/O
   */
  public HeimdallSubsystemIOSim(PhotonCamera camera) {
    super(camera);

    // Create the vision system simulation which handles cameras and targets on the field.
    visionSim = new VisionSystemSim("main");
    // Add all the AprilTags inside the tag layout as visible targets to this simulated field.
    visionSim.addAprilTags(HeimdallSubsystem.kTagLayout);
    // Create simulated camera properties. These can be set to mimic your actual camera.
    var cameraProp = new SimCameraProperties();
    cameraProp.setCalibration(960, 720, Rotation2d.fromDegrees(90));
    cameraProp.setCalibError(0.35, 0.10);
    cameraProp.setFPS(15);
    cameraProp.setAvgLatencyMs(50);
    cameraProp.setLatencyStdDevMs(15);
    // Create a PhotonCameraSim which will update the linked PhotonCamera's values with visible
    // targets.
    cameraSim = new PhotonCameraSim(camera, cameraProp);
    // Add the simulated camera to view the targets on this simulated field.
    visionSim.addCamera(cameraSim, HeimdallSubsystem.kRobotToCameraTransform);

    cameraSim.enableDrawWireframe(true);
  }

  //////////////////////////////////////////////////////////////////////////////////////////////////
  /** Called when an updated pose estimate is calculated */
  @Override
  protected void onUpdatedPoseEstimate(EstimatedRobotPose updatedEstimate) {
    // Update the dashboard display of the pose estimate in simulation mode
    visionSim
        .getDebugField()
        .getObject("VisionEstimation")
        .setPose(updatedEstimate.estimatedPose.toPose2d());
  }

  //////////////////////////////////////////////////////////////////////////////////////////////////
  /** Called when an updated pose estimate is missing */
  @Override
  protected void onMissingUpdatedPoseEstimate() {
    // Update the dashboard display of the pose estimate in simulation mode
    visionSim.getDebugField().getObject("VisionEstimation").setPoses();
  }
}
