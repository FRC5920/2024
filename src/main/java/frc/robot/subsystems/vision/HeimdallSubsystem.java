package frc.robot.subsystems.vision;

import edu.wpi.first.apriltag.AprilTagFieldLayout;
import edu.wpi.first.apriltag.AprilTagFields;
import edu.wpi.first.math.Matrix;
import edu.wpi.first.math.VecBuilder;
import edu.wpi.first.math.Vector;
import edu.wpi.first.math.geometry.Pose2d;
import edu.wpi.first.math.geometry.Rotation3d;
import edu.wpi.first.math.geometry.Transform3d;
import edu.wpi.first.math.geometry.Translation3d;
import edu.wpi.first.math.numbers.N1;
import edu.wpi.first.math.numbers.N3;
import edu.wpi.first.math.util.Units;
import edu.wpi.first.wpilibj2.command.SubsystemBase;

/** The HeimdallSubsystem provides a computer vision subsystem using the PhotonVision library */
public class HeimdallSubsystem extends SubsystemBase {

  ////////////////////////////////////
  // CONSTANTS
  ////////////////////////////////////

  /** Physical location of the camera on the robot, relative to the center of the robot. */
  public static final Transform3d kRobotToCameraTransform =
      new Transform3d(new Translation3d(0.5, 0.0, 0.5), new Rotation3d(0, 0, 0));

  // The layout of the AprilTags on the field
  public static final AprilTagFieldLayout kTagLayout =
      AprilTagFields.kDefaultField.loadAprilTagLayoutField();

  // The standard deviations of our vision estimated poses, which affect correction rate
  // (Fake values. Experiment and determine estimation noise on an actual robot.)
  public static final Matrix<N3, N1> kSingleTagStdDevs = VecBuilder.fill(4, 4, 8);
  public static final Matrix<N3, N1> kMultiTagStdDevs = VecBuilder.fill(0.5, 0.5, 1);

  /** I/O implementation used by the subsystem */
  private final HeimdallSubsystemIO m_io;

  /** Inputs updated by the subsystem */
  private final HeimdallSubsystemInputs m_inputs = new HeimdallSubsystemInputs();

  // Kalman Filter Configuration. These can be "tuned-to-taste" based on how much
  // you trust your various sensors. Smaller numbers will cause the filter to
  // "trust" the estimate from that particular component more than the others.
  // This in turn means the particualr component will have a stronger influence
  // on the final pose estimate.

  /**
   * Standard deviations of the vision measurements. Increase these numbers to trust global
   * measurements from vision less. This matrix is in the form [x, y, theta]ᵀ, with units in meters
   * and radians.
   */
  private Vector<N3> visionMeasurementStdDevs =
      VecBuilder.fill(0.5, 0.5, Units.degreesToRadians(10));

  private double previousPipelineTimestamp = 0;
  private boolean addPose = false;

  //////////////////////////////////////////////////////////////////////////////////////////////////
  /**
   * Creates an instance of the subsystem using a specified I/O implementation
   *
   * @param io I/O implementation to be used by the subsystem
   */
  public HeimdallSubsystem(HeimdallSubsystemIO io) {
    m_io = io;
  }

  //////////////////////////////////////////////////////////////////////////////////////////////////
  @Override
  public void periodic() {
    m_io.updateInputs(m_inputs);
  }

  //////////////////////////////////////////////////////////////////////////////////////////////////
  public Pose2d getCurrentPose() {
    return m_inputs.estimatedPose.toPose2d();
  }

  //////////////////////////////////////////////////////////////////////////////////////////////////
  /**
   * Resets the current pose to the specified pose. This should ONLY be called when the robot's
   * position on the field is known, like at the beginning of a match.
   *
   * @param newPose new pose
   */
  public void setPose(Pose2d newPose) {
    m_io.setPose(newPose);
  }

  //////////////////////////////////////////////////////////////////////////////////////////////////
  /**
   * Resets the position on the field to 0,0 0-degrees, with forward being downfield. This resets
   * what "forward" is for field oriented driving.
   */
  public void resetPose() {
    setPose(new Pose2d());
  }
}
