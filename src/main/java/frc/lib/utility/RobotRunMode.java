/////////////////////////////////////////////////////////////////////////////
/**
 * Modes the robot can run in
 *
 * @details used to switch between running the bot in real/simulation or log replay mode
 */
public enum RobotRunMode {
  /** Running on real robot hardware */
  REAL,

  /** Replaying data from a robot run */
  REPLAY,

  /** Running in a robot simulation */
  SIM;

  /** Get the human-readable name of the robot type */
  @Override
  public String toString() {
    return this.name();
  }
};
