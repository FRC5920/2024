package frc.lib.utility;

import com.pathplanner.lib.path.PathPlannerTrajectory;
import edu.wpi.first.math.trajectory.Trajectory;
import edu.wpi.first.math.trajectory.Trajectory.State;
import java.util.ArrayList;
import java.util.List;

/** A helper class used to store settings and load a Trajectory file */
public class TrajectoryUtil {

  /** Returns a WPILib Trajectory object containing the states from a PathPlannerTrajectory */
  public static Trajectory getWPITrajectory(PathPlannerTrajectory ppTrajectory) {
    List<PathPlannerTrajectory.State> ppStates = ppTrajectory.getStates();
    List<State> wpiStates = new ArrayList<State>();

    for (PathPlannerTrajectory.State ppState : ppStates) {
      State wpiState =
          new State(
              ppState.timeSeconds,
              ppState.velocityMps,
              ppState.accelerationMpsSq,
              ppState.getTargetHolonomicPose(),
              ppState.curvatureRadPerMeter);
      wpiStates.add(wpiState);
    }

    return new Trajectory(wpiStates);
  }
}
