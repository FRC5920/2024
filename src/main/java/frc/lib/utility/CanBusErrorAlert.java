package frc.lib.utility;

import edu.wpi.first.hal.can.CANStatus;
import edu.wpi.first.wpilibj.Timer;

/** Add your docs here. */
public class CanBusErrorAlert extends Alert {
  public static final double kDefaultThresholdSec = 0.5; // Default threshold

  private final double m_timeThresholdSec = kDefaultThresholdSec; // Seconds to disable alert
  private final Timer m_errorTimer = new Timer();
  private final Timer m_initialTimer = new Timer();

  /** Creates an instance of the alert object */
  public CanBusErrorAlert(String alertMessage, AlertType alertType) {
    super(alertMessage, alertType);
    reset();
  }

  /** Resets the alert object and its timers */
  public void reset() {
    m_errorTimer.reset();
    m_errorTimer.start();
    m_initialTimer.reset();
    m_initialTimer.start();
  }

  /** Update the alert */
  public void update(CANStatus canStatus) {
    if (canStatus.receiveErrorCount > 0 || canStatus.transmitErrorCount > 0) {
      m_errorTimer.reset();
    }
    set(
        !m_errorTimer.hasElapsed(m_timeThresholdSec)
            && m_initialTimer.hasElapsed(m_timeThresholdSec));
  }
}
