package frc.lib.joystick;

/** Utility class used to feather values from a joystick axis */
public class AxisFeathering implements AxisProcessor {
  public static final double kDefaultSensitivity = 1.0;

  /** < Sensitivity 0.0(off) to 1.0(linear) */
  private double m_sensitivity;

  public AxisFeathering() {
    m_sensitivity = kDefaultSensitivity;
  }

  public AxisFeathering(double sensitivity) {
    m_sensitivity = sensitivity;
  }

  public double process(double axisValue) {
    // Sensitivity is in values of 0 to 1 and it is cubing the input.
    return ((1.0 - m_sensitivity) * axisValue) + (m_sensitivity * Math.pow(axisValue, 3));
  }

  public AxisFeathering setSensitivity(double sens) {
    m_sensitivity = sens;
    return this;
  }
}
